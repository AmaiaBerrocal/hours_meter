class Hour:
    def __init__(self, id_hours, id_user, year, month, day, hours, minutes, creation_date, modification_date):
        self.id_hours = id_hours
        self.id_user = id_user
        self.year = year
        self.month = month
        self.day = day
        self.hours = hours
        self.minutes = minutes
        self.creation_date = creation_date
        self.modification_date = modification_date